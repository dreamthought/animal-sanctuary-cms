        <p>
          <b>The Sanctuary is situated on 32 acres of organic property with lots of native bush, and is bordered by 400 acres of protected bush reserve. 
          This makes it a perfect bird site, so we are actively involved with the care of native birds such as tuis, wood pigeons, herons, moreporks, paradise ducks, etc.</b>
        </p>
        <p>
          We often get baby and juvenile birds that need mothering before they can be released. Each bird receives a specialized baby diet that at first 
          is given on the hour, progressing up to three times a day as they mature. Once they are eating on their own, 
          they are moved into the recovery aviaries where they begin flying and learning how to recognize native foods. 
          When they have the basics down, they are promoted to the larger flight aviary to build up their strength for release.
        </p>
        <p>
          Injured birds are first seen by a veterinarian for xrays and assessment. Birds requiring daily medication and individual care are kept close by 
          for regular treatement. Once stabilised, birds are placed in larger recovery aviaries to regain balance and movement. 
          The final step in their rehab program is learning to fly vertically and for longer stretches in the big aviary, which is necessary for a successful release.
        </p>
        <p>
          Our goal is the eventual release of all native birds in our care, and we support feed after release until the birds have successfully established 
          their new life in the wild. You can be here to see the release yourself by becoming a Native Bird Buddy and supporting The Sanctuary with a regular donation. 
